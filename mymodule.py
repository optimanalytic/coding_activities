def square(number):
    """
    Returns the square of a given number.

    Args:
        number (int or float): The number to be squared.

    Returns:
        int or float: The square of the input number.
    """
    return number ** 2


def double(number):
    """
    Returns twice the value of a given number.

    Args:
        number (int or float): The number to be doubled.

    Returns:
        int or float: Double the input number.
    """
    return number * 2
